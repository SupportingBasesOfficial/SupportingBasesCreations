import * as fs from "fs";
import * as path from "path";
import { fileURLToPath } from "url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const TEMPLATE_DIR = path.resolve(
  __dirname,
  "../../core/templates/base-template",
);

const OUTPUT_FILE = path.resolve(__dirname, "../src/base/packed-templates.ts");

const SKIP_DIRS = new Set([
  ".git",
  "node_modules",
  ".next",
  "dist",
  ".turbo",
  ".husky/_",
]);

const SKIP_FILES = new Set([".gitmodules", "pnpm-lock.yaml"]);

const files = [];

function walkDir(dir, base) {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  for (const entry of entries) {
    if (SKIP_DIRS.has(entry.name)) continue;
    const fullPath = path.join(dir, entry.name);
    const relativePath = path.relative(base, fullPath).replace(/\\/g, "/");
    if (entry.isDirectory()) {
      walkDir(fullPath, base);
    } else if (entry.isFile()) {
      if (SKIP_FILES.has(entry.name)) continue;
      const content = fs.readFileSync(fullPath, "utf-8");
      files.push({ path: relativePath, content });
    }
  }
}

if (!fs.existsSync(TEMPLATE_DIR)) {
  console.error("Template dir not found:", TEMPLATE_DIR);
  process.exit(1);
}

walkDir(TEMPLATE_DIR, TEMPLATE_DIR);

const output = `// AUTO-GENERATED by scripts/pack-templates.mjs — DO NOT EDIT
export interface PackedFile {
  path: string;
  content: string;
}

export const packedTemplates: PackedFile[] = ${JSON.stringify(files, null, 2)};
`;

fs.writeFileSync(OUTPUT_FILE, output);
console.log(`Packed ${files.length} template files to ${OUTPUT_FILE}`);
